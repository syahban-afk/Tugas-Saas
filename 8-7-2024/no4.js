// Destructuring Array
const buah = ["Apel", "Pisang", "Jeruk"];
const [buah1, buah2, buah3] = buah;
console.log(buah1, buah2, buah3);

// Destructuring Object
const alamat = { jalan: "Jl. Ciampea", kota: "Bogor", provinsi: "Jawa Barat" };
const { jalan, kota, provinsi } = alamat;
console.log(jalan, kota, provinsi);
